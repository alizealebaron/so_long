#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char		c;
	long int	nb;

	nb = n;
	if (nb < 0)
	{
		ft_putchar_fd('-', fd);
		nb = -nb;
	}
	if (nb > 9)
	{
		ft_putnbr_fd(nb / 10, fd);
	}
	c = (char) '0' + nb % 10;
	ft_putchar_fd(c, fd);
}
